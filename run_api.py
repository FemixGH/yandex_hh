current_dir = Path(__file__).parent
sys.path.insert(0, str(current_dir))

def main():
    """–ó–∞–ø—É—Å–∫ FastAPI —Å–µ—Ä–≤–µ—Ä–∞"""
    # –ü–∞—Ä–∞–º–µ—Ç—Ä—ã –∑–∞–ø—É—Å–∫–∞
    host = os.getenv("API_HOST", "127.0.0.1")
    port = int(os.getenv("API_PORT", "8000"))
    reload = os.getenv("API_RELOAD", "true").lower() == "true"
    log_level = os.getenv("API_LOG_LEVEL", "info")

    print(f"üöÄ –ó–∞–ø—É—Å–∫ –ò–ò –ë–∞—Ä–º–µ–Ω API –Ω–∞ http://{host}:{port}")
    print(f"üìö –î–æ–∫—É–º–µ–Ω—Ç–∞—Ü–∏—è –¥–æ—Å—Ç—É–ø–Ω–∞ –Ω–∞ http://{host}:{port}/docs")
    print(f"üîÑ –ê–≤—Ç–æ–ø–µ—Ä–µ–∑–∞–≥—Ä—É–∑–∫–∞: {'–≤–∫–ª—é—á–µ–Ω–∞' if reload else '–æ—Ç–∫–ª—é—á–µ–Ω–∞'}")

    uvicorn.run(
        "backend_api:app",
        host=host,
        port=port,
        reload=reload,
        log_level=log_level,
        access_log=True
    )

if __name__ == "__main__":
    main()
